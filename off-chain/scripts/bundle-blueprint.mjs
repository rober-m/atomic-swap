// Inlines the on-chain blueprint into a generated TypeScript module so the
// built library carries the validators with it — consumers importing the
// package from a frontend or backend don't have to supply plutus.json.
//
// Runs automatically before build/test/start (npm "pre" hooks). Reads
// ../blueprint/plutus.json if present; otherwise emits a null placeholder so
// the sources still type-check before the on-chain component has been built.
import { existsSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const blueprintPath = resolve(here, "..", "..", "blueprint", "plutus.json");
const outPath = resolve(here, "..", "src", "blueprint.generated.ts");

const header =
  "// AUTO-GENERATED by scripts/bundle-blueprint.mjs — do not edit by hand.\n" +
  "// Regenerated from ../blueprint/plutus.json before each build.\n" +
  'import type { Blueprint } from "./contract.js";\n\n';

let body;
if (existsSync(blueprintPath)) {
  const blueprint = JSON.parse(readFileSync(blueprintPath, "utf-8"));
  body = `export const bundledBlueprint: Blueprint | null = ${JSON.stringify(
    blueprint,
    null,
    2,
  )};\n`;
  console.log(`Bundled ${blueprint.validators?.length ?? 0} validators from blueprint.`);
} else {
  body = "export const bundledBlueprint: Blueprint | null = null;\n";
  console.log("No blueprint found; emitted a null placeholder.");
}

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, header + body);
